double a,b;
double f(double g){
    // speed = g
    return a/(sqrtl(g)) + ((g-1)*b);
}

void solve()
{
    cin >> a >> b;
    int l = 1, r = 1e18;
    
    while(l + 2 < r){
        int m1 = l+((r-l)/3), m2 = r-((r-l)/3);
        if(f(m1) < f(m2))r = m2;
        else l = m1;
    }
    double res = 1e18;
    for(int i = l;i<=r;++i)res= min(res, f(i));
    cout << fixed << setprecision(15) << res << endl;
}   

